package bean;

//데이터를 저장하는 클래스
public class loginBean {
	private String id;
	private String password;
//getter setter
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
}
